package poke.server.resources;

public class CourseDetails {

	private String courseId;
	private String courseDescp;
	private String courseProf;
	
	public String getCourseId() {
		return courseId;
	}
	public void setCourseId(String courseId) {
		this.courseId = courseId;
	}
	public String getCourseDescp() {
		return courseDescp;
	}
	public void setCourseDescp(String courseDescp) {
		this.courseDescp = courseDescp;
	}
	public String getCourseProf() {
		return courseProf;
	}
	public void setCourseProf(String courseProf) {
		this.courseProf = courseProf;
	}
}
